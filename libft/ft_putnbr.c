#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == INT_MIN)
	{
		write(1, "-2", 2);
		n = -n - 2000000000;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
